`timescale 1ns / 1ps

module ALU_tb;

reg  [1:0]  ALU_Op;
reg  [15:0] ALU_A;
reg  [15:0] ALU_B;

wire [15:0] ALU_Out;
wire Branch_Flag;

ALU uut (
    .ALU_Op(ALU_Op),
    .ALU_A(ALU_A),
    .ALU_B(ALU_B),
    .ALU_Out(ALU_Out),
    .Branch_Flag(Branch_Flag)
);

initial begin

//------------------------------
// ADD
// 13 + 7 = 20
//------------------------------
ALU_A = 16'd13;
ALU_B = 16'd7;
ALU_Op = 2'b00;
#10;

//------------------------------
// SUB
// 25 - 9 = 16
//------------------------------
ALU_A = 16'd25;
ALU_B = 16'd9;
ALU_Op = 2'b01;
#10;

//------------------------------
// AND
//------------------------------
ALU_A = 16'b1010110010101100;
ALU_B = 16'b1111000011110001;
ALU_Op = 2'b10;
#10;

//------------------------------
// OR
//------------------------------
ALU_A = 16'b1010101010101011;
ALU_B = 16'b0101010101010101;
ALU_Op = 2'b11;
#10;

//------------------------------
// BEQ (Result = 0)
//------------------------------
ALU_A = 16'd22;
ALU_B = 16'd22;
ALU_Op = 2'b01;
#10;

//------------------------------
// BNE (Result ≠ 0)
//------------------------------
ALU_A = 16'd22;
ALU_B = 16'd5;
ALU_Op = 2'b01;
#10;

$finish;

end

endmodule