`timescale 1ns / 1ps

module datapath_tb;

//------------------------------------------------
// Inputs
//------------------------------------------------

reg  [1:0]  ALU_Op;
reg         ALU_Src;

reg  [15:0] ALU_A;
reg  [15:0] Reg_In;
reg  [7:0]  Imm_In;

//------------------------------------------------
// Internal wires
//------------------------------------------------

wire [15:0] Imm_Out;
wire [15:0] ALU_B;
wire [15:0] ALU_Out;
wire        Branch_Flag;

//------------------------------------------------
// Instantiate Sign Extender
//------------------------------------------------

SignExtender sign_ext (
    .Imm_In(Imm_In),
    .Imm_Out(Imm_Out)
);

//------------------------------------------------
// Instantiate MUX
//------------------------------------------------

ALUSrc_MUX alu_mux (
    .ALU_Src(ALU_Src),
    .Imm_In(Imm_Out),
    .Reg_In(Reg_In),
    .Src_Out(ALU_B)
);

//------------------------------------------------
// Instantiate ALU
//------------------------------------------------

ALU alu (
    .ALU_Op(ALU_Op),
    .ALU_A(ALU_A),
    .ALU_B(ALU_B),
    .ALU_Out(ALU_Out),
    .Branch_Flag(Branch_Flag)
);

//------------------------------------------------
// Stimulus
//------------------------------------------------

initial begin

